/* 算術演算子 */

// 加算（addition）
let add = 10 + 5;
console.log(add);

// 文字列の連結
let str = "Java" + "Script";
console.log(str);

let result1 = 3 + 3;
let result2 = "3" + "3";

console.log(result1);
console.log(result2);

// 減算（subtract）
let sub = 10 - 5;
console.log(sub);

//乗算（multiplication）
let mul = 10 * 5;
console.log(mul);

// 除算（division）
let div = 10 / 5;
console.log(div);

// インクリメント（increment）
let incr = 10;
incr++;
console.log(incr);

// デクリメント（decrement）
let decr = 10;
decr--;
console.log(decr);

/* 
    代入演算子
*/

// 加算して代入
let add2 = 1;
add2 += 2;
console.log(add2);

// 文字列を連結して代入
let str2 = "Java";
str2 += "Script";
console.log(str2);

// 減算して代入
let sub2 = 10;
sub2 -= 5;
console.log(sub2);